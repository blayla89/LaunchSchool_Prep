// Interpolation with template literals
let firstName = 'Victor'
console.log(`Good Morning, ${firstName}.`); 
console.log(`Good Afternoon, ${firstName}.`); 
console.log(`Good Evening, ${firstName}.`); 

// Concatenation with + operater and string literals 
let first = 'Victor'; 
console.log('Good Morning, ' + first + '.')
console.log('Good Afternoon, ' + first + '.')
console.log('Good Evening, ' + first + '.')
